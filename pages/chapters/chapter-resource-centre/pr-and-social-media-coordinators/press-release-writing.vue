    <h1>Press Release Writing</h1>
    <h2>For the media</h2>
    A press release, is a written or recorded piece of communication written by an organisation which is directed at members of the news media for the purpose of announcing something newsworthy and including the announcement in their publication

    <h2>For the reader</h2>
    It is also the only way of conveying all of your key messages and information you want to put into the public domain for your audience You, as a chapter, may want to use this opportunity to highlight
